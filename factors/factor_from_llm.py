        df['regime_state'] = np.sign(df['position_zscore'] * df['volume_zscore'])
        
        # 计算状态持续性
        df['regime_persistence'] = df['regime_state'].rolling(persistence_window).sum() / persistence_window
        
        # 价格动量确认
        df['price_momentum'] = df['close'].diff(regime_window) / df['close'].shift(regime_window)
        df['momentum_alignment'] = np.sign(df['regime_state'] * df['price_momentum'])
        
        # 持仓变化强度
        df['position_change_intensity'] = df['position'].pct_change().abs()
        
        # 成交量强度
        df['volume_intensity'] = df['volume'] / df['volume'].rolling(regime_window).mean()
        
        # 综合持续性指数
        df['factor'] = df['regime_persistence'] * df['momentum_alignment'] * df['position_change_intensity'] * df['volume_intensity']
        
        # 范围调整
        df['range_ratio'] = (df['high'] - df['low']) / df['close']
        df['factor'] = df['factor'] * (1 + df['range_ratio'])
        
        return df['factor']


