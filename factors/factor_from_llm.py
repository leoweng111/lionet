class fac_position_accumulation_efficiency:
    param_range = {'window': [10, 20, 30], 'threshold': [0.1, 0.2, 0.3]}
    
    @staticmethod
    def operate(Data: pd.DataFrame, **kwargs):
        hash_tb = {chr(i): 0 for i in range(97, 123)}
        for key, value in kwargs.items():
            hash_tb[key] = value
        window = int(hash_tb['window'])
        threshold = float(hash_tb['threshold'])
        
        df = Data.copy()
        # 持仓积累：持仓增加时的价格变化效率
        position_change = df['position'].diff()
        price_change = df['close'].pct_change()
        
        # 只考虑持仓显著变化的情况
        position_change_pct = position_change / df['position'].shift(1).replace(0, 1)
        significant_change = position_change_pct.abs() > threshold
        
        # 积累效率：持仓增加时的正向价格变化比率
        accumulation_efficiency = pd.Series(0, index=df.index)
        for i in range(1, len(df)):
            if significant_change.iloc[i]:
                if position_change.iloc[i] > 0:
                    # 持仓增加时，价格应该上涨才有效率
                    efficiency = price_change.iloc[i] if price_change.iloc[i] > 0 else -price_change.iloc[i]
                else:
                    # 持仓减少时，价格应该下跌才有效率
                    efficiency = -price_change.iloc[i] if price_change.iloc[i] < 0 else price_change.iloc[i]
                accumulation_efficiency.iloc[i] = efficiency
        
        # 滚动平均和标准化
        factor = accumulation_efficiency.rolling(window).mean()
        factor = (factor - factor.rolling(window*2).mean()) / factor.rolling(window).std().replace(0, 1)
        
        return factor


